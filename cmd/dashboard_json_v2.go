func fixDashboardJson() ([]byte, error) {
	inner := make(map[string]interface{})
	err := json.Unmarshal([]byte(dashboardJSON), &inner)
	if err != nil {
		return nil, err
	}
	// clear ID for posting
	inner["id"] = nil

	outter := make(map[string]interface{})
	outter["dashboard"] = inner
	outter["overwrite"] = true

	return json.Marshal(outter)
}

